#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		i;
	char	arr[10];

	i = 0;
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	while (nb > 0)
	{
		arr[i] = '0' + (nb % 10);
		nb /= 10;
		i++;
	}
	i--;
	while (i >= 0)
	{
		write(1, &arr[i], 1);
		i--;
	}
}
