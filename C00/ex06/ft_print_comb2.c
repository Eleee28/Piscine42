#include <unistd.h>

void	ft_print_comb2(void)
{
	char	c[3];

	c[0] = 0;
	c[1] = ' ';
	while (c[0] <= 99)
	{
		c[2] = c[0] + 1;
		while (c[2] <= 99)
		{
			if (!(c[0] / 10 > c[2] / 10) && (!(c[0] % 10 > c[2] % 10)))
			{
				write(1, &(char){'0' + (c[0] / 10)}, 1);
				write(1, &(char){'0' + (c[0] % 10)}, 1);
				write(1, &c[1], 1);
				write(1, &(char){'0' + (c[2] / 10)}, 1);
				write(1, &(char){'0' + (c[2] % 10)}, 1);
				if (c[0] < 98)
					write(1, ", ", 2);
			}
			c[2]++;
		}
		c[0]++;
	}
}

int main()
{
	ft_print_comb2();
	return (0);
}